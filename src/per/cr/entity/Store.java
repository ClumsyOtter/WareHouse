package per.cr.entity;

public class Store {
	private int sid;
	private String id;
	private String name;
	private String locat;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLocat() {
		return locat;
	}

	public void setLocat(String locat) {
		this.locat = locat;
	}

	public int getSid() {
		return sid;
	}

}
